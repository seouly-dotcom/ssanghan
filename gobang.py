def ensure_data_exists():
    """데이터 파일이 이상하면 무조건 강제로 다시 씁니다."""
    try:
        # 무조건 덮어쓰기 (가장 확실한 방법)
        with open(DATA_FILE, 'w', encoding='utf-8') as f:
            f.write(DEFAULT_DATA.strip())
    except Exception as e:
        print(f"파일 생성 중 오류 발생: {e}")

def load_data():
    """CSV 파일을 불러옵니다."""
    ensure_data_exists()  # 실행할 때마다 파일 복구
    
    try:
        return pd.read_csv(DATA_FILE, encoding='utf-8')
    except:
        return pd.read_csv(io.StringIO(DEFAULT_DATA))

def parse_herbs(herb_str):
    herbs = {}
    if pd.isna(herb_str): return herbs
    items = str(herb_str).split(',')
    for item in items:
        try:
            if ':' in item:
                name, weight = item.strip().split(':')
                herbs[name.strip()] = float(weight.strip())
            else:
                herbs[item.strip()] = 0.0
        except: continue
    return herbs

def combine_formulas(formula_names_list):
    """합방 계산 로직"""
    df = load_data()
    if df.empty: return None
    
    combined_herbs = {}
    found_any = False
    
    for name in formula_names_list:
        name = name.strip()
        row = df[df['처방명'] == name]
        
        if not row.empty:
            found_any = True
            herb_str = row.iloc[0]['구성약재']
            herbs = parse_herbs(herb_str)
            
            for herb, weight in herbs.items():
                combined_herbs[herb] = combined_herbs.get(herb, 0) + weight
    
    return combined_herbs if found_any else None

def search_formula(keyword):
    """처방 검색"""
    df = load_data()
    if df.empty: return None
    
    mask = df['처방명'].str.contains(keyword, na=False) | df['효능'].str.contains(keyword, na=False)
    return df[mask]
